int sum(int arry[N][N])//????? ?????? ???????  ???? ?????
{                      //???????? ????? ????? ?????? ????? ????? (N-1)?? ????? ?????? ?????? ???? ? - , 
	int i, j, count = 0, temp = 0, sum = 0;
	for (j = 0; j < N; j++)
	{
		for (i = 1; count != arry[0][j]; i++)
		{
			if (i == N && arry[0][j] > N - 1)
			{
				i = 1;
			}
			temp = temp + arry[i][j];
			count++;
		}
		if (temp >= sum)
		{
			sum = temp;
		}
		temp = 0;
		count = 0;

	}
	return(sum);
}

